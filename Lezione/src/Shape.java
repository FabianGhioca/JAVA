public class Shape {    
    
    private int id;
    double area;
    
    public void setId(int id) {
        this.id = id;
    }
    
    public int getId() {
        return id;
    }
    
    public void setArea(double area) {
        this.area = area;
    }
    
    public double getArea() {
        return this.area;
    }


}
