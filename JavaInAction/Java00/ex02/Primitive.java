package JavaInAction.Java00.ex02;

public class Primitive {
    public static void main(String[] args) {
    int myInt = 42;
    double myDouble = 3.14;
    char myChar = 'A';
    boolean myBoo = true;
    byte myByte = 10;
    short myShort = 3000;
    long myLong = 123456789L;
    float myFloat = 2.718f;

    System.out.println (
        "myInt: " + myInt + "\n" +
        "MyDouble: " + myDouble + "\n" +
        "My char: " + myChar + "\n" +
        "MyBoo: " + myBoo + "\n" +
        "MyByte: " + myByte + "\n" +
        "MyShort: " + myShort + "\n" +
        "MyLong: " + myLong + "\n" +
        "MyFloat: " + myFloat );
    } 
}
